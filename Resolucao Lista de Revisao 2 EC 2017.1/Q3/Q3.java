/*
a) Uma classe representa um conjunto de objetos com as mesmas características e comportamentos. Uma classe é definida pelo seu nome, 
pelo seu conjunto de atributos e de métodos. Objetos são instâncias de classes. No paradigma de orientação a objetos, tudo pode ser, 
potencialmente, representado como um objeto.

b) Um método nada mais é que o equivalente a um procedimento ou uma função (também podem ter um retorno), com a restrição que ele 
pode manipular apenas os parâmetros passados a ele e os atributos que foram definidos para o objeto. Já, os atributos são 
características de um objeto. Basicamente, são a estrutura de dados que vai representar a classe. Definem, também, o estado do objeto 
em um determinado instante.

c) Um programa orientado a objetos é composto por um conjunto de objetos que interagem através de "trocas de mensagens". Na prática,
essa troca de mensagem ocorre através da chamada aos métodos de um objeto por outro objeto.

d) Na orientação a objetos, o uso da encapsulação e ocultamento da informação recomenda que a representação do estado de um objeto
deve ser mantida oculta. Cada objeto deve ser manipulado exclusivamente através dos métodos públicos do objeto, dos quais apenas a
assinatura deve ser revelada. O conjunto de assinaturas dos métodos públicos da classe constitui sua interface operacional. Dessa forma,
detalhes internos sobre a operação do objeto não são conhecidos, permitindo que o usuário trabalhe em um nível mais alto de abstração, 
sem preocupação com os detalhes internos da classe. Essa facilidade permite simplificar a construção de programas com funcionalidades 
complexas, tais como interfaces gráficas ou aplicações distribuídas.

e) Herança ou generalização é o mecanismo pelo qual uma classe (subclasse) pode estender outra classe (superclasse), aproveitando
seus comportamentos (métodos) e características (atributos). Já o Polimorfismo é o princípio pelo qual duas ou mais classes derivadas
de uma mesma superclasse podem invocar métodos que têm a  mesma identificação (assinatura), mas comportamentos distintos, especializados
para cada classe derivada, usando para tanto uma referência a um objeto do tipo da superclasse. Esse mecanismo é fundamental na programação
orientada a objetos, permitindo definir funcionalidades que operem genericamente com objetos, abstraindo-se de seus detalhes particulares 
quando esses não forem necessários.
*/