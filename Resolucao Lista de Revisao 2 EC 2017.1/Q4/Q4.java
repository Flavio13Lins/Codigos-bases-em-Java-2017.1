/*
Coesão e acoplamento são dois atributos importantes de projetos em engenharia de software e linguagens. Módulos coesos são aqueles que
possuem poucas responsabilidades. Desta forma a manutenção é mais simples e evita efeitos colaterais. Fica mais fácil alterar uma parte
da aplicação sem afetar outras partes. Por isto é confundido com o princípio da responsabilidade única. É importante tirar funcionalidade
supérfluas de um módulo e transferir para outro modulo.

Já o atributo do acomplamento se refere ao relacionamento entre os módulos. Indica quanto uma módulo depende de outro para funcionar. 
Códigos desacoplados são aqueles de relação fraca, e não dependem de outros para fazer sua funcionalidade básica.

Todo mundo que já viu orientação a objetos alguma vez na vida conhece a frase "Classes devem ter alta coesão e baixo acoplamento". De fato,
acoplar-se a uma classe significa depender dela. E o problema de depender de outras classes é que, caso elas mudem, a classe principal pode
sofrer mudanças também. A ideia, então, é reduzir o acoplamento entre os classes o máximo possível.

Vale, ainda, lembrar que estes conceitos existem pelo menos desde a década de 60. E ao contrário do que muitos acreditam, não é um conceito
intrínseco da orientação a objeto.
*/